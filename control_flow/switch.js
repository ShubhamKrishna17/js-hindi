// SWITCH statement
let month = 6;

switch (month) {
    case 1:
        console.log(`Month is Jan`);
        break;
    case 2:
        console.log(`Month is Feb`);
        break;
    case 3:
        console.log(`Month is Mar`);
        break;
    case 4:
        console.log(`Month is Apr`);
        break;

    default:
        console.log(`No month found`);
        break;
}